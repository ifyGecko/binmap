void draw_text(SDL_Renderer *r, int x, int y, int scale, SDL_Color color, const char *text)
{
    SDL_SetRenderDrawColor(r, color.r, color.g, color.b, color.a);
    int cx = x;
    for (const char *p = text; *p; p++) {
        unsigned char ch = (unsigned char)*p;
        if (ch >= 'a' && ch <= 'z') ch -= 32;
        if (ch >= 128) ch = '?';
        const uint8_t *glyph = font5x7[ch];
        for (int row = 0; row < FONT_H; row++) {
            uint8_t bits = glyph[row];
            if (!bits) continue;
            for (int col = 0; col < FONT_W; col++) {
                if (bits & (1u << (FONT_W - 1 - col))) {
                    SDL_FRect rr = {
                        (float)(cx + col * scale),
                        (float)(y  + row * scale),
                        (float)scale, (float)scale
                    };
                    SDL_RenderFillRect(r, &rr);
                }
            }
        }
        cx += (FONT_W + 1) * scale;
    }
}

int text_width(int scale, const char *text)
{
    int n = (int)strlen(text);
    if (n == 0) return 0;
    return n * (FONT_W + 1) * scale - scale;
}

void blit_text(uint32_t *pixels, int pw, int ph,
               int x, int y, int scale, uint32_t color, const char *text)
{
    int cx = x;
    for (const char *p = text; *p; p++) {
        unsigned char ch = (unsigned char)*p;
        if (ch >= 'a' && ch <= 'z') ch -= 32;
        if (ch >= 128) ch = '?';
        const uint8_t *glyph = font5x7[ch];
        for (int row = 0; row < FONT_H; row++) {
            uint8_t bits = glyph[row];
            if (!bits) continue;
            for (int col = 0; col < FONT_W; col++) {
                if (!(bits & (1u << (FONT_W - 1 - col)))) continue;
                int px0 = cx + col * scale;
                int py0 = y  + row * scale;
                for (int dy = 0; dy < scale; dy++) {
                    int yy = py0 + dy;
                    if (yy < 0 || yy >= ph) continue;
                    uint32_t *line = pixels + (size_t)yy * (size_t)pw;
                    for (int dx = 0; dx < scale; dx++) {
                        int xx = px0 + dx;
                        if (xx < 0 || xx >= pw) continue;
                        line[xx] = color;
                    }
                }
            }
        }
        cx += (FONT_W + 1) * scale;
    }
}
